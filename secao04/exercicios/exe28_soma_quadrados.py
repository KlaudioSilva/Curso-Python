"""
Leia três valores e mostre como resultado a soma dos
quadrados dos três valores lidos.
"""

n1 = int(input('Digite um valor inteiro qualquer: '))
n2 = int(input('Digite outro valor inteiro qualquer: '))
n3 = int(input('Digite mais um valor inteiro qualquer: '))
soma = (n1 ** 2) + (n2 ** 2) + (n3 ** 2)

print(f'O resultado da soma dos quadrados desses valores foi {soma}.')
print()
